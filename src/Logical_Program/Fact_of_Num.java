package Logical_Program;

public class Fact_of_Num {

	public static void main(String[] args) {
		
		
			
		int num =5;     //5*4*3*2*1
		int fact =1;
		
		for (int i=num; i>=1; i--)
		
		{
			fact =fact * i;  // 5*4 =20
			                 // 20*3 =60
			                 // 60*2 =120
		}			
			System.out.println(fact);		
					
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}


